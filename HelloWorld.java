public class HelloWorld {
    public static void main(String args[]){

        // hello world
        System.out.println("Hello World");

        //creating primitive data type variables
        int a = 10; // 4 byte in memory
        short b = 32767; // 2 bytes
        long c = 123578544734l; // 8 bytes
        byte d = 127; // 1 byte which is equal to 8 bits

        // 1 bit can store max of two values i.e. binary 0 or 1

        float f = 1.000001244f;
        double db = 1000000.00000012345d;

        boolean boo = true;
        char ch = 'j';

        //printing variables
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("f = " + f);
        System.out.println("db = " + db);
        System.out.println("boo = " + boo);
        System.out.println("ch = " + ch);
    }
}
